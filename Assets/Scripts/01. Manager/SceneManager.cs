        #region [FindScene] 
        //------------------------------------------------------------------------------------------------------------------------------------------------------
        public bool FindScene(string SceneName)
        {
            Scene sc = UnityEngine.SceneManagement.SceneManager.GetSceneByName(SceneName);
            if (sc != null)
                return true;
            else
                return false;
        }
